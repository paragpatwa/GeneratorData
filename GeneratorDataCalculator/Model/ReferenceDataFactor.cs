namespace GeneratorData.Model
{
    public class ReferenceDataFactor
    {
        private decimal highField;
        private decimal mediumField;
        private decimal lowField;

        public decimal High
        {
            get => this.highField;
            set => this.highField = value;
        }

        public decimal Medium
        {
            get => this.mediumField;
            set => this.mediumField = value;
        }

        public decimal Low
        {
            get => this.lowField;
            set => this.lowField = value;
        }
    }
}
